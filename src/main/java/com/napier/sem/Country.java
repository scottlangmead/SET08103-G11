package com.napier.sem;

/**
 * Attributes of a Country
 * @author Scott Langmead, Matthew Wilson, Md Saifullah
 */
public class Country
{
    /**
     * Default constructor
     */
    public Country() {}

    /**
     * Country's Code
     */
    private String code;
    /**
     * Country's Name
     */
    private String name;
    /**
     * Country's Continent
     */
    private String continent;
    /**
     * Country's Region
     */
    private String region;
    /**
     * Country's Total Surface Area
     */
    private int surfaceArea;
    /**
     * Country's Independence Year
     */
    private int indepYear;
    /**
     * Country Population
     */
    private int population;
    /**
     * Country's Life Expectancy
     */
    private int lifeExp;
    /**
     * Country's Gross National Product
     */
    private int gnp;
    /**
     * Country's Old Gross National Product
     */
    private int gnpOld;
    /**
     * Country's Local Name
     */
    private String localName;
    /**
     * Country's Government Form
     */
    private String governmentForm;
    /**
     * Country's Head Of State
     */
    private String headOfState;
    /**
     * Country's Capital
     */
    private String capital;
    /**
     * Country's Second Code
     */
    private String code2;

    /**
     * Updates the Country Code
     * @param code
     */
    public void setCode(String code) { this.code = code; }
    /**
     * Retrieves the Country Code
     * @return Country Code
     */
    public String getCode() { return code; }

    /**
     * Updates the Country Name
     * @param name
     */
    public void setName(String name) { this.name = name; }
    /**
     * Retrieves the Country Name
     * @return Country Name
     */
    public String getName() { return name; }

    /**
     * Updates the Country Continent
     * @param continent
     */
    public void setContinent(String continent) { this.continent = continent; }
    /**
     * Retrieves the Country Continent
     * @return Country Continent
     */
    public String getContinent() { return continent; }

    /**
     * Updates the Country Region
     * @param region
     */
    public void setRegion(String region) { this.region = region; }
    /**
     * Retrieves the Country Region
     * @return Country Region
     */
    public String getRegion() { return region; }

    /**
     * Updates the Country Surface Area
     * @param surfaceArea
     */
    public void setSurfaceArea(String surfaceArea) { this.region = surfaceArea; }
    /**
     * Retrieves the Country Surface Area
     * @return Country Surface Area
     */
    public int getSurfaceArea() { return surfaceArea; }

    /**
     * Updates the Country Independence Year
     * @param indepYear
     */
    public void setIndepYear(int indepYear) { this.indepYear = indepYear; }
    /**
     * Retrieves the Country Independence Year
     * @return Country Independence Year
     */
    public int getIndepYear() { return indepYear; }

    /**
     * Updates the Country Population
     * @param population
     */
    public void setPopulation(int population) { this.population = population; }
    /**
     * Retrieves the Country Population
     * @return Country Population
     */
    public int getPopulation() { return population; }

    /**
     * Updates the Country Life Expectancy
     * @param lifeExp
     */
    public void setLifeExp(int lifeExp) { this.lifeExp = lifeExp; }
    /**
     * Retrieves the Country Life Expectancy
     * @return Country Life Expectancy
     */
    public int getLifeExp() { return lifeExp; }

    /**
     * Updates the Country Gross National Product
     * @param gnp
     */
    public void setGnp(int gnp) { this.gnp = gnp; }
    /**
     * Retrieves the Country Gross National Product
     * @return Country Gross National Product
     */
    public int getGnp() { return gnp; }

    /**
     * Updates the Country Old Gross National Product
     * @param gnpOld
     */
    public void setGnpOld(int gnpOld) { this.gnpOld = gnpOld; }
    /**
     * Retrieves the Country Old Gross National Product
     * @return Country Old Gross National Product
     */
    public int getGnpOld() { return gnpOld; }

    /**
     * Updates the Country Local Name
     * @param localName
     */
    public void setLocalName(String localName) { this.localName = localName; }
    /**
     * Retrieves the Country Local Name
     * @return Country Local Name
     */
    public String getLocalName() { return localName; }

    /**
     * Updates the Country Government Form
     * @param governmentForm
     */
    public void setGovernmentForm(String governmentForm) { this.governmentForm = governmentForm; }
    /**
     * Retrieves the Country Government Form
     * @return Country Government Form
     */
    public String getGovernmentForm() { return governmentForm; }

    /**
     * Updates the Country Head of State
     * @param headOfState
     */
    public void setHeadOfState(String headOfState) { this.headOfState = headOfState; }
    /**
     * Retrieves the Country Head of State
     * @return Country Head of State
     */
    public String getHeadOfState() { return headOfState; }

    /**
     * Updates the Country Capital
     * @param capital
     */
    public void setCapital(String capital) { this.capital = capital; }
    /**
     * Retrieves the Country Capital
     * @return Country Capital
     */
    public String getCapital() { return capital; }

    /**
     * Updates the Country Second Code
     * @param code2
     */
    public void setCode2(String code2) { this.code2 = code2; }
    /**
     * Retrieves the Country Second Code
     * @return Country Second Code
     */
    public String getCode2() { return code2; }
}